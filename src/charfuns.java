
import java.io.*;
import java.util.*;
public class charfuns {
    public static void main(String[] args)
    {
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Character.MIN_VALUE);
        System.out.println(Character.SIZE);
        System.out.println(Character.BYTES);
        System.out.println(Character.valueOf('A'));
        System.out.println(Character.isLowerCase('A'));
        System.out.println(Character.isLowerCase(65));
        System.out.println(Character.toLowerCase('A'));
        System.out.println(Character.toLowerCase(65));
        System.out.println(Character.toUpperCase('a'));
        System.out.println(Character.toUpperCase(97));
        System.out.println(Character.isDigit(65));
        System.out.println(Character.isDigit('A'));
        System.out.println(Character.isDigit(49));
        System.out.println(Character.isLowerCase(65));
        System.out.println(Character.isWhitespace('a'));
        System.out.println(Character.isWhitespace(32));
        System.out.println(Character.isLetter(65));
        System.out.println(Character.isWhitespace(65));



    }
}
